#!/usr/bin/env bash
set -euo pipefail

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
JS_DIR="$(dirname "$SCRIPT_DIR")"

cd "$JS_DIR"

echo "================================================================"
echo "=== JS: Installing npm dependencies ==="
echo "================================================================"

npm install

echo ""
echo "================================================================"
echo "=== JS: Building Rust (Neon) and TypeScript ==="
echo "================================================================"

npm run build

echo ""
echo "================================================================"
echo "=== JS: Running tests ==="
echo "================================================================"

npm test

echo ""
echo "================================================================"
echo "=== JS check passed ==="
echo "================================================================"
